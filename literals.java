

//Selection of data type according to the minimum and maximum value which can be used for that variable
public class literals {
    public static void main(String[] args){
        //literal is the value stored in the variable
        byte a = 21;
        int b = 10;
        float c = 3.14f;
        double d = -2.3D;
        char ch = 'a';
        long nm = 49999999L;
        short n = 23;
        boolean me = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(ch);
        System.out.println(nm);
        System.out.println(n);
        System.out.println(me);
        String name = "Gopal";
        System.out.println(name);
    }
}
